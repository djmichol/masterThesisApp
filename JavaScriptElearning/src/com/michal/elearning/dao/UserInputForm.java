package com.michal.elearning.dao;

public class UserInputForm {
	
	private String boredom;
	private String engaged;
	private String frustration;
	private String confusion;
	public String getBoredom() {
		return boredom;
	}
	public void setBoredom(String boredom) {
		this.boredom = boredom;
	}
	public String getEngaged() {
		return engaged;
	}
	public void setEngaged(String engaged) {
		this.engaged = engaged;
	}
	public String getFrustration() {
		return frustration;
	}
	public void setFrustration(String frustration) {
		this.frustration = frustration;
	}
	public String getConfusion() {
		return confusion;
	}
	public void setConfusion(String confusion) {
		this.confusion = confusion;
	}

}
